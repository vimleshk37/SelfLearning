package javaUtilities;

public class ConstructorsExample {
	
	
	  public ConstructorsExample() { System.out.println("Default"
	  +" Constructors: No argument"); }
	  
	  public ConstructorsExample(int i) { System.out.println(i
	  +" Constructors: with argument"); }
	  
	  public ConstructorsExample(int i, int k) { System.out.println(i*k
	  +" Constructors: with argument"); }
	  
	  public ConstructorsExample(String name) { System.out.println(name
	  +" Constructors: with argument"); }
	 
}
